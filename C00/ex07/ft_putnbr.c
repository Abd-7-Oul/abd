#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		i;
	char	c[10];

	i = 0;
	if (nb == 0)
	{
		write(1, "0", 1);
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	while (nb > 0)
	{
		c[i] = (nb % 10) + '0';
		nb /= 10;
		i++;
	}
	i -= 1;
	while (i >= 0)
	{
		write(1, &c[i], 1);
		i--;
	}
}
